'''
La función 'square' se utiliza para dibujar una celda en la pantalla. 
Toma dos argumentos, x e y, que especifican las coordenadas de la celda. 
Luego, dibuja un cuadrado de tamaño 20x20 píxeles en la posición dada.
La función utiliza la instancia de Turtle llamada path para dibujar la celda.
'''
def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

 #Función que dibuja el mapa usando path.
def world():
    bgcolor('pink')
    path.color('black')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def add_ghosts():
    '''Se crea una función que añade dos fantasmas al juego'''
    ghosts.append([vector(-100, 160), vector(0, 5)])
    ghosts.append([vector(100, 160), vector(5, 0)])
    
'''Este bloque define una función llamada move que se utiliza para mover Pac-Man en la dirección especificada por la variable aim. 
La función también comprueba si Pac-Man ha chocado contra una pared o ha salido de los límites del tablero.
Si Pac-Man come una pepita, aumenta su puntuación en 1 punto.
'''
def move():
 
    #Se llama a la función add_ghosts en el momento que score = 20
    global state
    if state['score'] == 20 and len(ghosts) == 4:
        add_ghosts()
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return
#Si bajamos el numero, los fantasmas van más rapido, si lo subimos, los fantasas se alentan
    ontimer(move, 30)
    
'''La función change que se utiliza para cambiar la dirección en la que se mueve Pac-Man en función de las teclas de dirección que el jugador presiona.
La función actualiza la variable aim en consecuencia.'''
def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

